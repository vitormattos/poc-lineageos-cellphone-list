            <p>LineageOS is a free and open-source operating system for smartphones, tablet computers, and set-top boxes, based on the Android mobile platform. It is the successor to the custom ROM CyanogenMod, from which it was forked in December 2016 when Cyanogen Inc. announced it was discontinuing development and shut down the infrastructure behind the project. Since Cyanogen Inc. retained the rights to the Cyanogen name, the project rebranded its fork as LineageOS.</p>

            </p>LineageOS was officially launched on 24 December 2016, with the source code available on GitHub. As with all versions of Android, operating system releases are specific to a single model. Since its launch, LineageOS development builds are available for more than 185 phone models with over 1.7 million active installs, having doubled its user base in the months February–March 2017.</p>

